"""Module contenant des opérations mathématiques simples."""


class SimpleMath:
    """Classe pour effectuer des opérations mathématiques de base."""

    @staticmethod
    def addition(a, b):
        """
        Additionne deux nombres.
        
        Args:
            a: Premier nombre
            b: Deuxième nombre
            
        Returns:
            La somme de a et b
        """
        return a + b
